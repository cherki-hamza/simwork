function setLang(lang) {
  localStorage.setItem('simwork_lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Update active lang button
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.getAttribute('onclick').includes("'" + lang + "'"));
  });

  // Apply translations via data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const keyPath = el.getAttribute('data-i18n').split('.');
    if (keyPath.length === 2) {
      const section = keyPath[0];
      const key = keyPath[1];
      if (translations[section] && translations[section][lang] && translations[section][lang][key]) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          if (el.type === 'submit' || el.type === 'button') {
            el.value = translations[section][lang][key];
          } else {
            el.placeholder = translations[section][lang][key];
          }
        } else if (el.tagName === 'OPTION') {
            el.textContent = translations[section][lang][key];
        } else {
          el.innerHTML = translations[section][lang][key];
        }
      }
    }
  });
}

function initLang() {
  const savedLang = localStorage.getItem('simwork_lang') || 'fr';
  setLang(savedLang);
}

document.addEventListener('DOMContentLoaded', initLang);

// Setup Mobile Menu
document.addEventListener('DOMContentLoaded', () => {
  const hamburger = document.querySelector('.hamburger');
  const overlay = document.querySelector('.mobile-menu-overlay');
  
  if (hamburger && overlay) {
    hamburger.addEventListener('click', () => {
      overlay.classList.toggle('open');
      hamburger.innerHTML = overlay.classList.contains('open') ? '✕' : '☰';
    });
    
    overlay.querySelectorAll('a').forEach(link => {
      link.addEventListener('click', () => {
        overlay.classList.remove('open');
        hamburger.innerHTML = '☰';
      });
    });
  }

  // Scroll Reveal
  const revealEls = document.querySelectorAll('.reveal');
  if (revealEls.length > 0) {
    const observer = new IntersectionObserver(entries => {
      entries.forEach(e => {
        if (e.isIntersecting) {
          e.target.classList.add('active');
          observer.unobserve(e.target);
        }
      });
    }, { threshold: 0.1 });
    revealEls.forEach((el, i) => {
      el.style.transitionDelay = `${i % 10 * 0.05}s`;
      observer.observe(el);
    });
  }
});
